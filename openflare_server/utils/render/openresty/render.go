func RenderJSON(sourceJSON string, certificateFiles []SupportFile) (*Result, error) {
	var doc Document
	if err := json.Unmarshal([]byte(strings.TrimSpace(sourceJSON)), &doc); err != nil {
		return nil, fmt.Errorf("openresty source config json is invalid: %w", err)
	}
	return Render(doc, certificateFiles)
}

func Render(doc Document, certificateFiles []SupportFile) (*Result, error) {
	mainConfig := RenderMainConfig(doc.OpenRestyConfig)
	routeConfig, err := RenderRouteConfig(doc, certificateFiles)
	if err != nil {
		return nil, err
	}
	wafConfig, err := RenderWAFConfig(doc.WAF)
	if err != nil {
		return nil, err
	}
	powConfig, err := RenderPoWConfig(doc)
	if err != nil {
		return nil, err
	}
	files := append([]SupportFile(nil), certificateFiles...)
	files = append(files, SupportFile{Path: "pow_config.json", Content: powConfig})
	files = append(files, SupportFile{Path: "waf_config.json", Content: wafConfig})
	files = DedupeSupportFiles(files)
	return &Result{
		MainConfig:   mainConfig,
		RouteConfig:  routeConfig,
		SupportFiles: files,
		Checksum:     ChecksumBundle(mainConfig, routeConfig, files),
	}, nil
}

func RenderMainConfig(cfg ConfigSnapshot) string {
	templateText := cfg.MainConfigTemplate
	if strings.TrimSpace(templateText) == "" {
		templateText = defaultMainConfigTemplate
	}
	return renderMainConfigTemplate(templateText, cfg)
}

func ValidateMainConfigTemplate(templateText string) error {
	trimmed := strings.TrimSpace(templateText)
	if trimmed == "" {
		return errors.New("OpenRestyMainConfigTemplate 不能为空")
	}
	for _, placeholder := range requiredMainConfigTemplatePlaceholders {
		if !strings.Contains(trimmed, placeholder) {
			return fmt.Errorf("OpenRestyMainConfigTemplate 必须保留占位符 %s", placeholder)
		}
	}
	return nil
}

func RenderRouteConfig(doc Document, certificateFiles []SupportFile) (string, error) {
	var builder strings.Builder
	builder.WriteString("# This file is generated by OpenFlare. Do not edit manually.\n")
	certificates := certificatesByID(certificateFiles)
	for _, route := range doc.Routes {
		domains := normalizedRouteDomains(route)
		if len(domains) == 0 {
			return "", fmt.Errorf("route %s domains are invalid", route.Domain)
		}
		serverNames := renderServerNames(domains)
		displayName := strings.TrimSpace(route.SiteName)
		if displayName == "" {
			displayName = domains[0]
		}
		upstreams := route.Upstreams
		if len(upstreams) == 0 && strings.TrimSpace(route.OriginURL) != "" {
			upstreams = []string{route.OriginURL}
		}
		upstreamConfig := buildRouteUpstreamConfig(route, upstreams)
		if upstreamConfig.UsesNamedUpstream {
			builder.WriteString(renderNamedUpstreamBlock(upstreamConfig))
		}
		cacheConfig := routeCacheConfig{Enabled: route.CacheEnabled, Policy: route.CachePolicy, Rules: route.CacheRules}
		limitConfig := routeLimitConfig{LimitConnPerServer: route.LimitConnPerServer, LimitConnPerIP: route.LimitConnPerIP, LimitRate: route.LimitRate}
		powEnabled, _ := getPoWConfigForRoute(route.ID, doc.WAF)
		if route.PoWEnabled {
			powEnabled = true
		}
		if !route.EnableHTTPS {
			builder.WriteString(renderHTTPProxyServer(serverNames, displayName, route.OriginURL, route.OriginHost, route.CustomHeaders, cacheConfig, limitConfig, upstreamConfig, powEnabled, route.BasicAuthEnabled, route.BasicAuthUsername, route.BasicAuthPassword, doc.OpenRestyConfig))
			continue
		}
		certIDs := normalizeCertIDs(route.CertID, route.CertIDs)
		domainCertIDs := normalizeDomainCertIDs(domains, certIDs, route.DomainCertIDs)
		if len(certIDs) == 0 {
			return "", fmt.Errorf("路由 %s 未配置证书", route.Domain)
		}
		httpOnlyDomains := make([]string, 0, len(domains))
		domainsByCertID := make(map[uint][]string, len(certIDs))
		for index, domain := range domains {
			if index >= len(domainCertIDs) || domainCertIDs[index] == 0 {
				httpOnlyDomains = append(httpOnlyDomains, domain)
				continue
			}
			domainsByCertID[domainCertIDs[index]] = append(domainsByCertID[domainCertIDs[index]], domain)
		}
		for _, certID := range certIDs {
			assignedDomains := domainsByCertID[certID]
			if len(assignedDomains) == 0 {
				continue
			}
			certPEM, ok := certificates[certID]
			if !ok {
				return "", fmt.Errorf("route %s certificate %d does not exist", route.Domain, certID)
			}
			if err := validateCertificateCoverage(certPEM, assignedDomains); err != nil {
				return "", fmt.Errorf("site %s certificate validation failed: %w", displayName, err)
			}
		}
		if route.RedirectHTTP {
			if len(httpOnlyDomains) > 0 {
				builder.WriteString(renderHTTPProxyServer(renderServerNames(httpOnlyDomains), displayName, route.OriginURL, route.OriginHost, route.CustomHeaders, cacheConfig, limitConfig, upstreamConfig, powEnabled, route.BasicAuthEnabled, route.BasicAuthUsername, route.BasicAuthPassword, doc.OpenRestyConfig))
			}
			for _, certID := range certIDs {
				if assignedDomains := domainsByCertID[certID]; len(assignedDomains) > 0 {
					builder.WriteString(renderHTTPRedirectServer(renderServerNames(assignedDomains)))
				}
			}
		} else {
			builder.WriteString(renderHTTPProxyServer(serverNames, displayName, route.OriginURL, route.OriginHost, route.CustomHeaders, cacheConfig, limitConfig, upstreamConfig, powEnabled, route.BasicAuthEnabled, route.BasicAuthUsername, route.BasicAuthPassword, doc.OpenRestyConfig))
		}
		for _, certID := range certIDs {
			if assignedDomains := domainsByCertID[certID]; len(assignedDomains) > 0 {
				builder.WriteString(renderHTTPSServer(renderServerNames(assignedDomains), displayName, route.OriginURL, route.OriginHost, certID, route.CustomHeaders, cacheConfig, limitConfig, upstreamConfig, powEnabled, route.BasicAuthEnabled, route.BasicAuthUsername, route.BasicAuthPassword, doc.OpenRestyConfig))
			}
		}
	}
	return builder.String(), nil
}

func RenderPoWConfig(doc Document) (string, error) {
	type domainEntry struct {
		Domains []string   `json:"domains"`
		Enabled bool       `json:"enabled"`
		Config  *PoWConfig `json:"config"`
	}
	entries := make([]domainEntry, 0)
	for _, route := range doc.Routes {
		powEnabled, powConfig := getPoWConfigForRoute(route.ID, doc.WAF)
		if route.PoWEnabled {
			powEnabled = true
			if route.PoWConfig != nil {
				powConfig = route.PoWConfig
			}
		}
		if !powEnabled {
			continue
		}
		entries = append(entries, domainEntry{Domains: normalizedRouteDomains(route), Enabled: true, Config: powConfig})
	}
	if len(entries) == 0 {
		return "{}", nil
	}
	data, err := json.Marshal(entries)
	return string(data), err
}

func RenderWAFConfig(snapshot WAFDocument) (string, error) {
	type wafRuntimeRuleGroup struct {
		ID                uint     `json:"id"`
		Name              string   `json:"name"`
		IsGlobal          bool     `json:"is_global"`
		BlockStatusCode   int      `json:"block_status_code"`
		BlockResponseBody string   `json:"block_response_body"`
		IPWhitelist       []string `json:"ip_whitelist"`
		IPBlacklist       []string `json:"ip_blacklist"`
		IPWhitelistGroups []uint   `json:"ip_whitelist_group_ids,omitempty"`
		IPBlacklistGroups []uint   `json:"ip_blacklist_group_ids,omitempty"`
		CountryWhitelist  []string `json:"country_whitelist"`
		CountryBlacklist  []string `json:"country_blacklist"`
		RegionWhitelist   []string `json:"region_whitelist"`
		RegionBlacklist   []string `json:"region_blacklist"`
	}
	type wafRuntimeConfig struct {
		DefaultBlockStatusCode int                   `json:"default_block_status_code"`
		RuleGroups             []wafRuntimeRuleGroup `json:"rule_groups"`
		SiteRuleGroups         map[string][]uint     `json:"site_rule_groups"`
	}
	groups := make([]wafRuntimeRuleGroup, 0, len(snapshot.RuleGroups))
	globalGroupIDs := make([]uint, 0)
	enabledGroupIDs := make(map[uint]struct{}, len(snapshot.RuleGroups))
	for _, group := range snapshot.RuleGroups {
		if !group.Enabled {
			continue
		}
		statusCode := group.BlockStatusCode
		if statusCode == 0 {
			statusCode = defaultWAFBlockStatus
		}
		if group.IsGlobal {
			globalGroupIDs = append(globalGroupIDs, group.ID)
		}
		enabledGroupIDs[group.ID] = struct{}{}
		groups = append(groups, wafRuntimeRuleGroup{
			ID:                group.ID,
			Name:              group.Name,
			IsGlobal:          group.IsGlobal,
			BlockStatusCode:   statusCode,
			BlockResponseBody: group.BlockResponseBody,
			IPWhitelist:       sortedUniqueStrings(group.IPWhitelist),
			IPBlacklist:       sortedUniqueStrings(group.IPBlacklist),
			IPWhitelistGroups: sortedUniqueUintIDs(group.IPWhitelistGroups),
			IPBlacklistGroups: sortedUniqueUintIDs(group.IPBlacklistGroups),
			CountryWhitelist:  group.CountryWhitelist,
			CountryBlacklist:  group.CountryBlacklist,
			RegionWhitelist:   group.RegionWhitelist,
			RegionBlacklist:   group.RegionBlacklist,
		})
	}
	sort.Slice(groups, func(i, j int) bool {
		if groups[i].IsGlobal != groups[j].IsGlobal {
			return groups[i].IsGlobal
		}
		return groups[i].ID < groups[j].ID
	})
	sort.Slice(globalGroupIDs, func(i, j int) bool { return globalGroupIDs[i] < globalGroupIDs[j] })
	siteRuleGroups := make(map[string][]uint, len(snapshot.Bindings))
	for _, binding := range snapshot.Bindings {
		ids := append([]uint{}, globalGroupIDs...)
		for _, id := range binding.RuleGroupIDs {
			if _, ok := enabledGroupIDs[id]; ok {
				ids = append(ids, id)
			}
		}
		siteRuleGroups[binding.SiteName] = uniqueUintIDs(ids)
	}
	data, err := json.Marshal(wafRuntimeConfig{DefaultBlockStatusCode: defaultWAFBlockStatus, RuleGroups: groups, SiteRuleGroups: siteRuleGroups})
	return string(data), err
}

func sortedUniqueStrings(values []string) []string {
	items := append([]string{}, values...)
	items = uniqueStrings(items)
	sort.Strings(items)
	return items
}

func sortedUniqueUintIDs(values []uint) []uint {
	items := uniqueUintIDs(values)
	sort.Slice(items, func(i, j int) bool { return items[i] < items[j] })
	return items
}

func ChecksumBundle(mainConfig string, routeConfig string, supportFiles []SupportFile) string {
	var builder strings.Builder
	builder.WriteString(mainConfig)
	builder.WriteString("\n--route-config--\n")
	builder.WriteString(routeConfig)
	builder.WriteString("\n--support-files--\n")
	files := DedupeSupportFiles(supportFiles)
	sort.Slice(files, func(i int, j int) bool { return files[i].Path < files[j].Path })
	for _, file := range files {
		if file.Path == SourceConfigFileName {
			continue
		}
		builder.WriteString(file.Path)
		builder.WriteString("\n")
		builder.WriteString(file.Content)
		builder.WriteString("\n")
	}
	sum := sha256.Sum256([]byte(builder.String()))
	return hex.EncodeToString(sum[:])
}

func DedupeSupportFiles(files []SupportFile) []SupportFile {
	if len(files) == 0 {
		return nil
	}
	unique := make(map[string]SupportFile, len(files))
	for _, file := range files {
		unique[file.Path] = file
	}
	result := make([]SupportFile, 0, len(unique))
	for _, file := range unique {
		result = append(result, file)
	}
	return result
}

